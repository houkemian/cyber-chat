# 千禧年特供分隔符，缺一不可
_SEP: list[str] = ["の", "°", "~", "·", "_", "※"]


def generate_cyber_name() -> str:
    """
    随机拼装一个纯正千禧年风格的赛博网名。
    算法：词 A + 随机分隔符 + 词 B，两词相同则直接返回单词。
    """
    a = random.choice(_WORDS)
    b = random.choice(_WORDS)

    if a == b:
        # 撞词时保持纯粹，不硬拼
        return a

    sep = random.choice(_SEP)
    # 「の」作为最具年代感的分隔符，单独构成 "A の B" 格式
    if sep == "の":
        return f"{a}の{b}"

    return f"{a}{sep}{b}"
